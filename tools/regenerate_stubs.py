#!/usr/bin/env python3
"""
regenerate_stubs.py — DashDecomp
Replaces the broken Thumb-scan stubs with correct stubs derived from
Ghidra's retail binary analysis (retail_hashes.csv).

  • Uses retail_hashes.csv as the authoritative function list (15k+ entries)
  • Applies DLP xmap names where the VA is the same in both binaries (direct match)
  • Creates a stub for every function Ghidra found, named or anonymous

Usage:
    python tools/regenerate_stubs.py [--dry-run] [--apply-names-only]
"""
import argparse, csv, json, os, re, shutil
from pathlib import Path

ROOT      = Path(__file__).resolve().parent.parent
ASM_DIR   = ROOT / "asm"
BUILD     = ROOT / "build"
BASE_ADDR = 0x00100000

RETAIL_HASHES = BUILD / "retail_hashes.csv"
DLP_XMAP      = BUILD / "dlp_symbols/USA/CTRDash.xmap"

# Module address ranges (matches create_stubs.py)
MODULES = [
    ("System",  0x00100000, 0x001DFFFF),
    ("Sead",    0x001E0000, 0x0027FFFF),
    ("Race",    0x00280000, 0x0037FFFF),
    ("Kart",    0x00380000, 0x0046FFFF),
    ("Item",    0x00470000, 0x0050FFFF),
    ("UI",      0x00510000, 0x0057FFFF),
    ("Sound",   0x00580000, 0x005BFFFF),
    ("Net",     0x005C0000, 0x005DFFFF),
]

def va_to_module(va: int) -> str:
    for name, start, end in MODULES:
        if start <= va <= end:
            return name
    return "Misc"


def sanitize_stem(name: str) -> str:
    """Convert a C++ symbol to a safe filename stem."""
    n = name.replace("::", "_")
    n = re.sub(r"<[^>]*>", "", n)
    n = re.sub(r"\(.*?\)", "", n)
    n = re.sub(r"\b(void|bool|int|float|const|static|virtual|unsigned|char|inline)\b\s*", "", n)
    n = n.strip("_ ")
    n = re.sub(r"[^a-zA-Z0-9_]", "_", n)
    n = re.sub(r"__+", "_", n)
    return n[:80].strip("_")


STUB_TEMPLATE = """\
/* DashDecomp — NODECOMPILED stub
 * Function: {display_name}
 * Virtual Address: 0x{va:08X}
 * File Offset:     0x{offset:08X}
 * Size (bytes):    {size}
 *
 * Status: NODECOMPILED 🔴
 * This stub was auto-generated by tools/regenerate_stubs.py
 * Symbol source: {source}
 * To decompile: write matching C++ and replace this file with your .cpp in src/
 */

.section .text
.global {stem}
.type   {stem}, %function

{stem}:
    @ NODECOMPILED — assembly stub, not yet decompiled
    @ Size: {size} bytes at VA 0x{va:08X} in build/code.dec.bin
    bx lr
"""


def load_xmap(path: Path) -> dict[int, str]:
    syms: dict[int, str] = {}
    skip_re = re.compile(r"^(FUN_|DAT_|LAB_|switchD|caseD_|default|switchdata)")
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split(None, 1)
            if len(parts) == 2:
                addr = int(parts[0], 16)
                name = parts[1]
                if not skip_re.match(name):
                    syms[addr] = name
    return syms


def load_retail_functions(path: Path) -> list[dict]:
    funcs = []
    with open(path, newline="") as f:
        for row in csv.DictReader(f):
            funcs.append({
                "va":   int(row["address"], 16),
                "size": int(row["size"]),
                "ghidra_name": row["name"],
            })
    return funcs


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run",        action="store_true", help="Preview only, don't write files")
    parser.add_argument("--apply-names-only", action="store_true", help="Only rename existing stubs, don't create new ones")
    parser.add_argument("--min-size",       type=int, default=4, help="Min function size to create stub for")
    args = parser.parse_args()

    if not RETAIL_HASHES.exists():
        print(f"[ERR] Missing {RETAIL_HASHES} — run tools/ghidra/ExportFunctionHashes.java first")
        return

    print("[INFO] Loading DLP xmap symbols...")
    xmap = load_xmap(DLP_XMAP) if DLP_XMAP.exists() else {}
    print(f"[INFO] Named DLP symbols: {len(xmap):,}")

    print("[INFO] Loading retail function list from Ghidra...")
    retail_funcs = load_retail_functions(RETAIL_HASHES)
    print(f"[INFO] Retail functions (Ghidra): {len(retail_funcs):,}")

    # Find existing stubs
    existing_stubs: dict[int, Path] = {}
    for s in ASM_DIR.rglob("*.s"):
        if s.stem.startswith("sub_"):
            try:
                existing_stubs[int(s.stem[4:], 16)] = s
            except ValueError:
                pass

    print(f"[INFO] Existing stubs: {len(existing_stubs):,}")
    print()

    created = renamed = skipped = 0
    functions_json: dict[str, int] = {}

    for func in retail_funcs:
        va   = func["va"]
        size = func["size"]

        if size < args.min_size:
            skipped += 1
            continue

        module = va_to_module(va)
        offset = va - BASE_ADDR

        # Determine the best name
        xmap_name = xmap.get(va)
        if xmap_name:
            stem   = sanitize_stem(xmap_name)
            display_name = xmap_name
            source = "DLP xmap (direct VA match)"
        else:
            stem   = f"sub_{va:08X}"
            display_name = stem
            source = "Ghidra auto-analysis"

        if not stem:
            stem = f"sub_{va:08X}"

        stub_path = ASM_DIR / module / f"{stem}.s"

        # Avoid overwriting if file already exists with a non-auto name
        if stub_path.exists() and not stub_path.stem.startswith("sub_"):
            functions_json[str(stub_path.relative_to(ROOT))] = size
            skipped += 1
            continue

        # Collision guard
        if stub_path.exists():
            alt = ASM_DIR / module / f"{stem}_{va:08X}.s"
            stub_path = alt

        rel_path = str(stub_path.relative_to(ROOT))
        functions_json[rel_path] = size

        if args.dry_run:
            tag = "[DRY-CREATE]" if va not in existing_stubs else "[DRY-RENAME]"
            print(f"  {tag}  0x{va:08X}  {stub_path.name}  ({size} bytes)")
            created += 1
            continue

        if args.apply_names_only and va not in existing_stubs:
            skipped += 1
            continue

        # Remove old stub if it exists at this VA
        old_stub = existing_stubs.get(va)
        if old_stub and old_stub.exists() and old_stub != stub_path:
            old_stub.unlink()

        # Write new stub
        stub_path.parent.mkdir(parents=True, exist_ok=True)
        stub_path.write_text(STUB_TEMPLATE.format(
            display_name=display_name,
            va=va,
            offset=offset,
            size=size,
            stem=stem,
            source=source,
        ))

        if va in existing_stubs:
            renamed += 1
        else:
            created += 1

    if not args.dry_run:
        # Write functions.json
        json_path = ASM_DIR / "functions.json"
        with open(json_path, "w") as f:
            json.dump(dict(sorted(functions_json.items())), f, indent=2)
        print(f"[OK] Wrote {json_path.name}")

    print()
    print(f"[RESULT] Functions processed: {len(retail_funcs):,}")
    print(f"         Created:             {created:,}")
    print(f"         Renamed:             {renamed:,}")
    print(f"         Skipped:             {skipped:,}")
    print(f"         Direct xmap names:   {sum(1 for f in retail_funcs if f['va'] in xmap):,}")


if __name__ == "__main__":
    main()
