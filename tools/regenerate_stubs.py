#!/usr/bin/env python3
"""
regenerate_stubs.py — DashDecomp
Replaces stubs with correct ones derived from Ghidra's retail binary analysis.

Structure produced:
  asm/
    System/
      RootSystem/
        run.s          ← named C++ method
        start.s
      sub_XXXXXXXX.s   ← anonymous (no class info)
    Race/
      Field/
        ObjectDossun/
          doReaction_againstKart.s
    Sead/
      HeapMgr/
        getRootHeap.s

This hierarchy appears on decomp.dev as a drill-down tree:
  click System → click RootSystem → see individual methods

Usage:
    python tools/regenerate_stubs.py [--dry-run]
"""
import argparse, csv, json, re
from pathlib import Path

ROOT      = Path(__file__).resolve().parent.parent
ASM_DIR   = ROOT / "asm"
BUILD     = ROOT / "build"
BASE_ADDR = 0x00100000

RETAIL_HASHES = BUILD / "retail_hashes.csv"
DLP_XMAP      = BUILD / "dlp_symbols/USA/CTRDash.xmap"

# Module address ranges
MODULES = [
    ("System",  0x00100000, 0x001DFFFF),
    ("Sead",    0x001E0000, 0x0027FFFF),
    ("Race",    0x00280000, 0x0037FFFF),
    ("Kart",    0x00380000, 0x0046FFFF),
    ("Item",    0x00470000, 0x0050FFFF),
    ("UI",      0x00510000, 0x0057FFFF),
    ("Sound",   0x00580000, 0x005BFFFF),
    ("Net",     0x005C0000, 0x005DFFFF),
]

def va_to_module(va: int) -> str:
    for name, start, end in MODULES:
        if start <= va <= end:
            return name
    return "Misc"


def safe_part(s: str) -> str:
    """Make a single path component safe for the filesystem."""
    s = re.sub(r"[^a-zA-Z0-9_]", "_", s)
    s = re.sub(r"__+", "_", s)
    return s.strip("_")[:60]


def symbol_to_path(symbol: str) -> tuple[list[str], str]:
    """
    Parse a C++ symbol into (namespace_parts, method_stem).

    Examples:
      "System::RootSystem::run(const char*)"
        → (["System", "RootSystem"], "run")

      "nn::os::Initialize()"
        → (["nn", "os"], "Initialize")

      "nninitStartUp"
        → ([], "nninitStartUp")

      "Field::ObjectDossun::doReaction_againstKart(Field::EGHTReaction, ...)"
        → (["Field", "ObjectDossun"], "doReaction_againstKart")
    """
    # Strip return type / leading keywords
    s = re.sub(r"\b(static|virtual|inline|const)\b\s*", "", symbol).strip()

    # Remove template args
    s = re.sub(r"<[^<>]*>", "", s)

    # Remove parameter list (everything from first '(' onward)
    s = re.sub(r"\(.*", "", s).strip()

    # Split on ::
    parts = [p.strip() for p in s.split("::") if p.strip()]

    if not parts:
        return [], safe_part(symbol[:60])

    method = safe_part(parts[-1])
    namespaces = [safe_part(p) for p in parts[:-1] if p.strip()]

    # Remove empty parts
    namespaces = [n for n in namespaces if n]

    return namespaces, method


STUB_TEMPLATE = """\
/* DashDecomp — NODECOMPILED stub
 * Function: {display_name}
 * Virtual Address: 0x{va:08X}
 * File Offset:     0x{offset:08X}
 * Size (bytes):    {size}
 *
 * Status: NODECOMPILED 🔴
 * Auto-generated by tools/regenerate_stubs.py
 * Symbol source: {source}
 *
 * To decompile: write matching C++ and replace this file with your .cpp in src/
 */

.section .text
.global {label}
.type   {label}, %function

{label}:
    @ NODECOMPILED — assembly stub, not yet decompiled
    @ Size: {size} bytes at VA 0x{va:08X} in build/code.dec.bin
    bx lr
"""


def load_xmap(path: Path) -> dict[int, str]:
    syms: dict[int, str] = {}
    skip = re.compile(r"^(FUN_|DAT_|LAB_|switchD|caseD_|default|switchdata)")
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split(None, 1)
            if len(parts) == 2:
                addr = int(parts[0], 16)
                name = parts[1]
                if not skip.match(name):
                    syms[addr] = name
    return syms


def load_retail_functions(path: Path) -> list[dict]:
    funcs = []
    with open(path, newline="") as f:
        for row in csv.DictReader(f):
            funcs.append({
                "va":   int(row["address"], 16),
                "size": int(row["size"]),
            })
    return funcs


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--min-size", type=int, default=4)
    args = parser.parse_args()

    print("[INFO] Loading DLP xmap...")
    xmap = load_xmap(DLP_XMAP) if DLP_XMAP.exists() else {}
    print(f"[INFO] Named DLP symbols: {len(xmap):,}")

    print("[INFO] Loading Ghidra retail function list...")
    retail_funcs = load_retail_functions(RETAIL_HASHES)
    print(f"[INFO] Retail functions: {len(retail_funcs):,}")
    print()

    created = named = skipped = 0
    functions_json: dict[str, int] = {}
    # Track used paths to avoid collisions
    used_paths: dict[str, int] = {}

    for func in retail_funcs:
        va   = func["va"]
        size = func["size"]

        if size < args.min_size:
            skipped += 1
            continue

        module  = va_to_module(va)
        offset  = va - BASE_ADDR
        xmap_name = xmap.get(va)

        if xmap_name:
            # Named: build class-hierarchy path
            ns_parts, method = symbol_to_path(xmap_name)

            # Skip leading namespace if it duplicates the module name
            # e.g. System::RootSystem::run in module System → just RootSystem/run
            if ns_parts and ns_parts[0].lower() == module.lower():
                ns_parts = ns_parts[1:]

            # The module dir is the top-level; sub-namespaces form subdirs
            # e.g. System::RootSystem::run → asm/System/RootSystem/run.s
            # e.g. nn::os::Initialize      → asm/System/nn/os/Initialize.s
            subdir = ASM_DIR / module
            for part in ns_parts:
                subdir = subdir / part

            label        = f"{method}_{va:08X}" if not method else method
            stem         = label
            display_name = xmap_name
            source       = "DLP xmap (direct VA match)"
            named += 1
        else:
            # Anonymous: flat in module dir
            subdir       = ASM_DIR / module
            stem         = f"sub_{va:08X}"
            label        = stem
            display_name = stem
            source       = "Ghidra auto-analysis"

        stub_path = subdir / f"{stem}.s"

        # Collision guard
        rel = str(stub_path.relative_to(ROOT))
        if rel in used_paths:
            stem      = f"{stem}_{va:08X}"
            label     = stem
            stub_path = subdir / f"{stem}.s"
            rel       = str(stub_path.relative_to(ROOT))
        used_paths[rel] = va

        functions_json[rel] = size

        if args.dry_run:
            tag = "[NAMED]" if xmap_name else "[ANON]"
            print(f"  {tag}  0x{va:08X}  {stub_path.relative_to(ASM_DIR)}  ({size}B)")
            created += 1
            continue

        stub_path.parent.mkdir(parents=True, exist_ok=True)
        stub_path.write_text(STUB_TEMPLATE.format(
            display_name=display_name,
            va=va, offset=offset, size=size,
            label=label, source=source,
        ))
        created += 1

    if not args.dry_run:
        # Preserve any manually-written non-sub_ stubs already in asm/
        for existing in ASM_DIR.rglob("*.s"):
            rel = str(existing.relative_to(ROOT))
            if rel not in functions_json and not existing.stem.startswith("sub_"):
                # Keep it, estimate size from file (or 0)
                functions_json[rel] = 0

        json_path = ASM_DIR / "functions.json"
        with open(json_path, "w") as f:
            json.dump(dict(sorted(functions_json.items())), f, indent=2)
        print(f"[OK] Wrote {json_path.name}")

    print()
    print(f"[RESULT] Functions:   {len(retail_funcs):,}")
    print(f"         Created:     {created:,}")
    print(f"         Named:       {named:,}  (with class hierarchy)")
    print(f"         Anonymous:   {created - named:,}  (sub_XXXXXXXX, flat)")
    print(f"         Skipped:     {skipped:,}  (< {args.min_size} bytes)")


if __name__ == "__main__":
    main()
