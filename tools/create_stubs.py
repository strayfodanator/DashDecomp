#!/usr/bin/env python3
"""
DashDecomp — Function Stub Generator
Scans code.dec.bin for Thumb function prologues and creates
one NODECOMPILED .s stub per detected function.

This populates the asm/ tree with gray blocks for decomp.dev,
giving contributors a full visual map of the project's complexity.

Usage:
  python tools/create_stubs.py
  python tools/create_stubs.py --dry-run      # preview count only
  python tools/create_stubs.py --threshold 4  # min function size (bytes)
"""

import argparse
import struct
from pathlib import Path

# ── Paths ─────────────────────────────────────────────────────────────────────
ROOT     = Path(__file__).parent.parent
CODE_BIN = ROOT / "build" / "code.dec.bin"
ASM_DIR  = ROOT / "asm"

# ── ARM memory map ─────────────────────────────────────────────────────────────
BASE_ADDR = 0x00100000  # 3DS user code load address

# ── Module address buckets (rough split — will be refined after Ghidra) ───────
# These ranges are estimates based on typical MK7 binary layout.
# Contributors can move stubs to the correct module after symbol analysis.
MODULES = [
    ("System",  0x00100000, 0x001DFFFF),  # App init, OS wrappers, allocators
    ("Sead",    0x001E0000, 0x0027FFFF),  # Nintendo sead engine library
    ("Race",    0x00280000, 0x0037FFFF),  # Race director, course, camera
    ("Kart",    0x00380000, 0x0046FFFF),  # Kart physics, movement, collision
    ("Item",    0x00470000, 0x0050FFFF),  # Item system, boxes, projectiles
    ("UI",      0x00510000, 0x0057FFFF),  # Menus, HUD, text rendering
    ("Sound",   0x00580000, 0x005BFFFF),  # Audio system
    ("Net",     0x005C0000, 0x005DFFFF),  # Nintendo Network / NEX
]


def va_to_module(va: int) -> str:
    """Return the module name for a given virtual address."""
    for name, start, end in MODULES:
        if start <= va <= end:
            return name
    return "System"  # fallback


def detect_thumb_functions(data: bytes) -> list[int]:
    """
    Detect Thumb function entry points by scanning for prologue patterns.

    Thumb-16 PUSH with LR saved (most common function entry):
      Encoding: xxxx_xxxx 1011_0101  (second byte = 0xB5 = PUSH {..., LR})

    Thumb-32 PUSH.W with LR (larger register lists):
      Encoding: 0xE92D (big-endian word), followed by register mask with LR bit

    We also look for the simpler `PUSH {LR}` = 0x00 0xB5 (pure LR save).
    """
    entries = []
    n = len(data)
    i = 0

    while i < n - 2:
        b0 = data[i]
        b1 = data[i + 1]

        # ── Thumb-16: PUSH {rlist, LR} ────────────────────────────────────────
        # Format: push {rlist} with LR bit → second byte = 0xB5
        # First byte = register list (any subset of r0-r7 is valid)
        if b1 == 0xB5:
            # Sanity: 2-byte aligned entry (Thumb code is always even-aligned)
            if i % 2 == 0:
                entries.append(i)
            i += 2
            continue

        # ── Thumb-32: PUSH.W {rlist, LR} ─────────────────────────────────────
        # Encoding: E9 2D xx xx  (stmdb sp!, {rlist}) where LR bit (bit14) set
        if i + 4 <= n and b0 == 0xE9 and b1 == 0x2D:
            if i % 2 == 0:
                high_reg = data[i + 3]
                if high_reg & 0x40:  # bit 14 = LR register
                    entries.append(i)
            i += 4
            continue

        # ── Thumb-32: PUSH.W alternative (F8 4D E0) pattern ──────────────────
        if i + 4 <= n and b0 == 0xF8 and b1 == 0x4D:
            if i % 2 == 0:
                if data[i + 3] == 0xE0:  # PUSH.W {LR} (PC-relative)
                    entries.append(i)
            i += 4
            continue

        i += 2  # advance by Thumb halfword

    return entries


def write_stub(path: Path, name: str, va: int, offset: int) -> None:
    """Write a minimal NODECOMPILED assembly stub."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        f"/* DashDecomp — NODECOMPILED stub\n"
        f" * Function: {name}\n"
        f" * Virtual Address: 0x{va:08X}\n"
        f" * File Offset:     0x{offset:08X}\n"
        f" *\n"
        f" * Status: NODECOMPILED 🔴\n"
        f" * This stub was auto-generated by tools/create_stubs.py\n"
        f" * To decompile: create a scratch at decomp.me and write matching C++.\n"
        f" * Then replace this file with your .cpp in src/ and open a PR.\n"
        f" */\n"
        f"\n"
        f".section .text\n"
        f".global {name}\n"
        f".type   {name}, %function\n"
        f"\n"
        f"{name}:\n"
        f"    @ NODECOMPILED — assembly stub, not yet decompiled\n"
        f"    @ See the original asm at VA 0x{va:08X} in build/code.dec.bin\n"
        f"    bx lr\n"
    )


def main():
    parser = argparse.ArgumentParser(description="Generate NODECOMPILED .s stubs for all detected functions")
    parser.add_argument("--dry-run",   action="store_true", help="Preview count without writing files")
    parser.add_argument("--threshold", type=int, default=4, help="Minimum bytes between function entries (default: 4)")
    parser.add_argument("--limit",     type=int, default=0, help="Limit number of stubs to create (0 = all)")
    args = parser.parse_args()

    if not CODE_BIN.exists():
        print(f"[ERR] {CODE_BIN} not found. Run 'make extract' first.")
        return

    print(f"[INFO] Reading {CODE_BIN.name} ({CODE_BIN.stat().st_size // 1024} KB)...")
    data = CODE_BIN.read_bytes()

    print("[INFO] Scanning for Thumb function prologues...")
    raw_entries = detect_thumb_functions(data)

    # Filter out entries that are too close together (noise)
    entries = [raw_entries[0]] if raw_entries else []
    for va in raw_entries[1:]:
        if va - entries[-1] >= args.threshold:
            entries.append(va)

    if args.limit > 0:
        entries = entries[:args.limit]

    print(f"[INFO] Detected {len(entries)} function entry points.")
    print()

    # Count per module
    module_counts = {name: 0 for name, *_ in MODULES}
    module_counts["System"] = module_counts.get("System", 0)  # ensure fallback exists

    for offset in entries:
        va = BASE_ADDR + offset
        mod = va_to_module(va)
        module_counts[mod] = module_counts.get(mod, 0) + 1

    print(f"  {'Module':<12} {'Functions':>12}")
    print(f"  {'──────':<12} {'─────────':>12}")
    total = 0
    for mod, count in module_counts.items():
        print(f"  {mod:<12} {count:>12,}")
        total += count
    print(f"  {'TOTAL':<12} {total:>12,}")
    print()

    if args.dry_run:
        print("[DRY RUN] No files written.")
        return

    # Check if stubs already exist
    existing = list(ASM_DIR.rglob("sub_*.s"))
    if existing:
        print(f"[WARN] {len(existing)} stub files already exist. Skipping existing ones.")

    existing_names = {f.stem for f in existing}

    written = 0
    skipped = 0

    for offset in entries:
        va = BASE_ADDR + offset
        mod = va_to_module(va)
        name = f"sub_{va:08X}"

        if name in existing_names:
            skipped += 1
            continue

        # Sub-organize by address quartile within module (Actor, Physics, etc.)
        # For now, flat within module — contributors will reorganize with Ghidra
        stub_path = ASM_DIR / mod / f"{name}.s"

        write_stub(stub_path, name, va, offset)
        written += 1

        if written % 500 == 0:
            print(f"  ... wrote {written} stubs so far")

    print(f"\n[OK]  Done! Wrote {written} new stubs, skipped {skipped} existing.")
    print(f"      Run 'make progress' to see the updated dashboard.")
    print(f"      Run 'python tools/generate_report.py' to update report.json.")


if __name__ == "__main__":
    main()
